    private static async Task<Workload[]> VrpAsync(CancellationToken ct)
    {
        var graphService = new SpatialGraphService(NullLogger<SpatialGraphService>.Instance);
        var service = new FleetRoutingService(graphService, NullLogger<FleetRoutingService>.Instance);

        var shipped = await service.GetScenarioAsync("fullday", ct);
        var graph = await graphService.GetRedlandsGraphAsync(ct);

        var shippedRequest = new OptimizeRequestDto
        {
            DepotLatitude = shipped.DepotLatitude,
            DepotLongitude = shipped.DepotLongitude,
            Stops = shipped.Stops,
            VehicleCount = shipped.VehicleCount,
            VehicleCapacity = shipped.VehicleCapacity,
            ShiftStartMinutes = shipped.ShiftStartMinutes,
            ShiftEndMinutes = shipped.ShiftEndMinutes,
            VehicleFixedCost = shipped.VehicleFixedCost,
            MaxIterations = 5000
        };

        // A 120-stop instance built from real network nodes, matching the size the previously
        // published figure claimed to measure.
        var rng = new Lcg(20_260_707u);
        var stops = new List<FleetStopDto>(120);
        for (var i = 0; i < 120; i++)
        {
            var node = graph.Nodes[rng.Range(0, graph.Nodes.Count)];
            var ready = rng.Range(480.0, 800.0);
            stops.Add(new FleetStopDto
            {
                Id = i + 1,
                Label = $"Stop {i + 1:D3}",
                Latitude = node.Latitude,
                Longitude = node.Longitude,
                Demand = rng.Range(20, 150),
                ReadyMinutes = ready,
                DueMinutes = ready + rng.Range(90.0, 180.0),
                ServiceMinutes = rng.Range(8, 15)
            });
        }

        var bigRequest = new OptimizeRequestDto
        {
            DepotLatitude = shipped.DepotLatitude,
            DepotLongitude = shipped.DepotLongitude,
            Stops = stops,
            VehicleCount = 10,
            VehicleCapacity = 1600,
            ShiftStartMinutes = 480,
            ShiftEndMinutes = 1020,
            VehicleFixedCost = 25,
            MaxIterations = 5000
        };

        // As with the p-median: `repeats` solves, timed by the summed solver stage, with the
        // distance-matrix build in between excluded.
        async Task<RunOutcome> ScoreAsync(OptimizeRequestDto request, int repeats, CancellationToken token)
        {
            var stageMs = 0.0;
            var checksum = 0.0;
            var native = true;
            for (var i = 0; i < repeats; i++)
            {
                var r = await service.OptimizeAsync(request, token);
                stageMs += r.SolveMs;
                native &= r.NativeAccelerated;
                checksum = r.FinalObjective + r.TotalDistanceKm + r.TotalDurationMinutes + r.VehiclesUsed
                    + FoldIds(r.Routes.SelectMany(x => x.StopIds)) * 1e-9
                    + FoldIds(r.UnservedStopIds);
            }
            return new RunOutcome(checksum, stageMs, native);
        }

        const string note = "solver stage only (service-reported SolveMs); the shared road-network distance-matrix build is excluded";

        return
        [
            new Workload
            {
                Kernel = "vrp_solver_kernel",
                Name = "CVRPTW solve, 120 stops, 10 vehicles, 8 solves",
                Note = note,
                Run = token => ScoreAsync(bigRequest, 8, token)
            },
            new Workload
            {
                Kernel = "vrp_solver_kernel",
                Name = $"CVRPTW solve, shipped \"fullday\" preset, {shipped.Stops.Count} stops, 30 solves",
                Note = note,
                Run = token => ScoreAsync(shippedRequest, 30, token)
            }
        ];
    }
