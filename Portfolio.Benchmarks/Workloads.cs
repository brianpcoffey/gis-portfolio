    // ---------------------------------------------------------------- network_trace_kernel

    private static async Task<Workload[]> NetworkTraceAsync(CancellationToken ct)
    {
        var service = new OutageTraceService(NullLogger<OutageTraceService>.Instance);
        var network = await service.GetNetworkAsync(ct);

        // Every closed, non-tie element is faulted in turn. One iteration is a full sweep of
        // the circuit, which is both the realistic operational question ("what does each
        // possible fault cost me?") and large enough to time.
        var faultIds = network.Elements
            .Where(e => !e.IsOpen && e.DeviceType != DeviceTypes.TieSwitch)
            .Select(e => e.Id)
            .ToArray();

        return
        [
            new Workload
            {
                Kernel = "network_trace_kernel",
                Name = $"downstream trace sweep, {network.Elements.Count:N0} elements x {faultIds.Length:N0} faults",
                Run = async token =>
                {
                    var checksum = 0.0;
                    var native = true;
                    foreach (var faultId in faultIds)
                    {
                        var r = await service.TraceAsync(new TraceRequestDto
                        {
                            Elements = network.Elements,
                            SourceNodeId = network.SourceNodeId,
                            FaultElementId = faultId
                        }, token);
                        native &= r.NativeAccelerated;
                        checksum += r.CustomersAffected
                            + FoldIds(r.DownstreamElementIds) * 1e-9
                            + FoldIds(r.IsolationDeviceIds) * 1e-9;
                    }
                    return new RunOutcome(checksum, NativeAccelerated: native);
                }
            },
            new Workload
            {
                Kernel = "network_trace_kernel",
                Name = $"trace + restoration plan, {faultIds.Length:N0} faults",
                Run = async token =>
                {
                    var checksum = 0.0;
                    var native = true;
                    const int step = 1;
                    for (var i = 0; i < faultIds.Length; i += step)
                    {
                        var trace = await service.TraceAsync(new TraceRequestDto
                        {
                            Elements = network.Elements,
                            SourceNodeId = network.SourceNodeId,
                            FaultElementId = faultIds[i]
                        }, token);

                        var restore = await service.ProposeRestorationAsync(new RestoreRequestDto
                        {
                            Elements = network.Elements,
                            SourceNodeId = network.SourceNodeId,
                            FaultElementId = faultIds[i],
                            IsolationDeviceIds = trace.IsolationDeviceIds,
                            AssumedRepairMinutes = 180
                        }, token);

                        native &= trace.NativeAccelerated && restore.NativeAccelerated;
                        checksum += restore.CustomersRestored + restore.CustomersStillOut
                            + restore.TieElementId
                            + FoldIds(restore.EnergizedElementIds) * 1e-9;
                    }
                    return new RunOutcome(checksum, NativeAccelerated: native);
                }
            }
        ];
    }

