/// <summary>Result of a single workload invocation.</summary>
/// <param name="Checksum">
/// A deterministic fold of the actual result. Native and managed must agree; this is the
/// parity check and it is what makes a speedup number mean anything.
/// </param>
/// <param name="StageMilliseconds">
/// When set, the harness uses this instead of its own stopwatch. Two services report the
/// duration of the kernel stage itself (<c>SolveMs</c>), which is the only way to time the
/// solver without also timing the shared distance-matrix build that precedes it.
/// </param>
/// <param name="NativeAccelerated">
/// What the service reported about its own path, when the DTO exposes it. Used to verify the
/// native child process really ran native and the disabled child really did not.
/// </param>
internal readonly record struct RunOutcome(
    double Checksum,
    double? StageMilliseconds = null,
    bool? NativeAccelerated = null);

internal sealed class Workload
{
    public required string Kernel { get; init; }
    public required string Name { get; init; }
    public required Func<CancellationToken, Task<RunOutcome>> Run { get; init; }

    /// <summary>Free-text caveat rendered into the published table (timing scope, missing surface, …).</summary>
    public string? Note { get; init; }
}

/// <summary>
/// Builds every workload against the public service APIs.
///
/// <para>
/// Nothing here reaches into <c>Portfolio.Services</c> internals: the in-repo datasets
/// (<c>SoCalPolicyBook</c>, <c>RedlandsRoadNetwork</c>, <c>RedlandsDeliveryScenario</c>,
/// <c>RedlandsDistributionNetwork</c>, <c>RedlandsResponseScenario</c>,
/// <c>SyntheticChangeScene</c>) are all <c>internal</c>, and each is reached through the
/// public <c>Get…Async</c> that serves it. Where no in-repo dataset exists for a kernel the
/// input is generated by a fixed-seed LCG so the run is byte-repeatable.
/// </para>
/// </summary>
internal static class Workloads
{
    // A single fixed-seed generator so every synthetic input is identical on every run and
    // in both child processes. Never seeded from time, never from Random.Shared.
    private sealed class Lcg(uint seed)
    {
        private long _state = seed == 0 ? 1u : seed;

        public double NextDouble()
        {
            _state = _state * 48271 % 2147483647L;
            return (_state - 1) / 2147483646.0;
        }

        public double Range(double min, double max) => min + (max - min) * NextDouble();

        public int Range(int minInclusive, int maxExclusive)
            => minInclusive + (int)(NextDouble() * (maxExclusive - minInclusive));
    }

    public static async Task<List<Workload>> BuildAsync(CancellationToken ct)
    {
        var list = new List<Workload>();

        list.AddRange(await ScoringAsync(ct));
        list.AddRange(GeoStream());
        list.AddRange(Geometry());
        list.AddRange(Terrain());
        list.AddRange(await GraphAsync(ct));
        list.AddRange(Cluster());
        list.AddRange(Viewshed());
        list.AddRange(Overlay());
        list.AddRange(await CatRiskAsync(ct));
        list.AddRange(await ChangeDetectionAsync(ct));
        list.AddRange(await NetworkTraceAsync(ct));
        list.AddRange(await FacilityLocationAsync(ct));
        list.AddRange(await VrpAsync(ct));

        return list;
    }

    // ---------------------------------------------------------------- checksums

    private static double Fold(IEnumerable<double> values)
    {
        var sum = 0.0;
        foreach (var v in values)
            sum += v;
        return sum;
    }

    /// <summary>Order-sensitive hash over an integer sequence, folded into a double exactly.</summary>
    private static double FoldIds(IEnumerable<int> ids)
    {
        // Kept inside 2^53 so the double holds it exactly and the comparison is bit-equal.
        long h = 1469598103;
        foreach (var id in ids)
            h = (h * 31 + id) % 9007199254740881L;
        return h;
    }

    // ---------------------------------------------------------------- portfolio_scoring

    private static Task<Workload[]> ScoringAsync(CancellationToken ct)
    {
        // HomeScoringService is the one kernel whose only public entry point takes its input
        // from IPropertyRepository. There is no in-repo dataset for it and no seeded
        // generator behind a public API, so the harness supplies a deterministic in-memory
        // repository. The kernel under test — the weighted scoring loop — is unchanged.
        var repo = new DeterministicPropertyRepository(count: 200_000, seed: 20_260_719u);
        var service = new HomeScoringService(repo, NullLogger<HomeScoringService>.Instance, TimeProvider.System);

        var prefs = new HomeSearchPreferencesDto
        {
            MaxPrice = 2_000_000,
            MaxMonthlyBudget = 9_000,
            MinBedrooms = 1,
            MinBathrooms = 1,
            MinSqft = 400,
            MaxCommuteMin = 120
        };

        return Task.FromResult<Workload[]>(
        [
            new Workload
            {
                Kernel = "portfolio_scoring",
                Name = "weighted scoring, 200,000 properties, top 50",
                Note = "input from a deterministic in-memory IPropertyRepository (no in-repo dataset exists for this service)",
                Run = async token =>
                {
                    var top = await service.GetTopPropertiesAsync(prefs, 50, token);

                    // Deliberately order-insensitive. The service ranks on a composite score
                    // rounded to two decimals, so across 200,000 listings the top 50 contains
                    // exact ties, and the two paths order tied entries differently while
                    // agreeing on every score. Sum of scores plus sum of ids catches any real
                    // divergence — a different property entering the top 50 moves both — without
                    // reporting a tie-break as a correctness failure.
                    return new RunOutcome(
                        Fold(top.Select(p => p.CompositeScore))
                        + top.Count
                        + top.Sum(p => (double)p.PropertyId));
                }
            }
        ]);
    }

    // ---------------------------------------------------------------- geostream_processor

    private static Workload[] GeoStream()
    {
        var service = new GeoStreamProcessorService(NullLogger<GeoStreamProcessorService>.Instance);

        // The service caps a batch at 10,000 events, and that cap is a production guardrail
        // rather than a benchmark artifact — so the workload is a burst of full-size batches,
        // which is also what a real ingest loop does. Per-call marshalling is counted, on
        // purpose: it is part of what the caller pays.
        const int eventCount = 10_000;
        const int batches = 40;

        var rng = new Lcg(20_260_101u);
        var events = new List<TelemetryEventDto>(eventCount);
        var start = new DateTime(2026, 1, 1, 8, 0, 0, DateTimeKind.Utc);
        for (var i = 0; i < eventCount; i++)
        {
            events.Add(new TelemetryEventDto
            {
                EntityId = i % 4_000,
                TimestampUtc = start.AddMilliseconds(i * 7),
                Latitude = rng.Range(33.90, 34.20),
                Longitude = rng.Range(-117.40, -117.05),
                SpeedMetersPerSecond = rng.Range(0, 60),
                HeadingDegrees = rng.Range(0, 360)
            });
        }

        var request = new GeoStreamBatchRequestDto
        {
            Events = events,
            GridSizeDegrees = 0.005,
            AnomalySpeedThresholdMetersPerSecond = 45
        };

        return
        [
            new Workload
            {
                Kernel = "geostream_processor",
                Name = $"parse + filter + grid aggregate, {batches} x {eventCount:N0} events",
                Run = async token =>
                {
                    var checksum = 0.0;
                    var native = true;
                    for (var b = 0; b < batches; b++)
                    {
                        var r = await service.ProcessBatchAsync(request, token);
                        native &= r.NativeAccelerated;
                        checksum = r.ValidEvents + r.InvalidEvents * 3.0 + r.AnomalyCount * 7.0
                            + Fold(r.Aggregates.Select(a => a.AverageSpeedMetersPerSecond + a.MaxSpeedMetersPerSecond))
                            + FoldIds(r.Aggregates.Select(a => a.CellX * 7919 + a.CellY * 31 + a.Count));
                    }
                    return new RunOutcome(checksum, NativeAccelerated: native);
                }
            }
        ];
    }

    // ---------------------------------------------------------------- spatial_geometry_kernel

    private static Workload[] Geometry()
    {
        var service = new SpatialGeometryService(NullLogger<SpatialGeometryService>.Instance);

        // The service caps geometry input at 5,000 points, so one call is far below timer
        // resolution. The workload is a burst of full-size calls.
        const int vertexCount = 5_000;
        const int calls = 400;

        var rng = new Lcg(20_260_202u);
        var fan = new List<CoordinateDto>(vertexCount);
        for (var i = 0; i < vertexCount; i++)
        {
            var theta = i * 2.0 * Math.PI / vertexCount;
            var r = 1.0 + 0.05 * rng.NextDouble();
            fan.Add(new CoordinateDto { X = r * Math.Cos(theta), Y = r * Math.Sin(theta) });
        }

        var triangulate = new GeometryPointSetDto { Points = fan };

        var subject = new List<CoordinateDto>(vertexCount);
        for (var i = 0; i < vertexCount; i++)
        {
            var theta = i * 2.0 * Math.PI / vertexCount;
            var r = 1.0 + 0.6 * Math.Sin(theta * 9.0);
            subject.Add(new CoordinateDto { X = r * Math.Cos(theta), Y = r * Math.Sin(theta) });
        }

        var clip = new PolygonClipRequestDto
        {
            Subject = subject,
            MinX = -0.9,
            MinY = -0.9,
            MaxX = 0.9,
            MaxY = 0.9
        };

        return
        [
            new Workload
            {
                Kernel = "spatial_geometry_kernel",
                Name = $"fan triangulation, {calls} x {vertexCount:N0} vertices",
                Run = async token =>
                {
                    var checksum = 0.0;
                    var native = true;
                    for (var c = 0; c < calls; c++)
                    {
                        var r = await service.TriangulateAsync(triangulate, token);
                        native &= r.NativeAccelerated;
                        checksum = r.Triangles.Count + Fold(r.Triangles.Select(t => t.A.X + t.B.Y + t.C.X));
                    }
                    return new RunOutcome(checksum, NativeAccelerated: native);
                }
            },
            new Workload
            {
                Kernel = "spatial_geometry_kernel",
                Name = $"bounding-box clip, {calls} x {vertexCount:N0}-vertex polygon",
                Run = async token =>
                {
                    var checksum = 0.0;
                    var native = true;
                    for (var c = 0; c < calls; c++)
                    {
                        var r = await service.ClipToBoundingBoxAsync(clip, token);
                        native &= r.NativeAccelerated;
                        checksum = r.Vertices.Count + Fold(r.Vertices.Select(v => v.X * 0.5 + v.Y));
                    }
                    return new RunOutcome(checksum, NativeAccelerated: native);
                }
            }
        ];
    }

    // ---------------------------------------------------------------- raster_terrain_kernel

    private static Workload[] Terrain()
    {
        var service = new RasterTerrainService(NullLogger<RasterTerrainService>.Instance);

        // 250,000 cells is the service's raster ceiling; 500x500 sits exactly on it.
        const int dim = 500;
        const int hillshadeCalls = 20;
        var elevation = new List<double>(dim * dim);
        for (var y = 0; y < dim; y++)
        {
            for (var x = 0; x < dim; x++)
            {
                var fx = x / (double)dim;
                var fy = y / (double)dim;
                elevation.Add(
                    420.0
                    + 260.0 * Math.Sin(fx * 6.0) * Math.Cos(fy * 4.5)
                    + 70.0 * Math.Sin(fx * 23.0 + fy * 17.0));
            }
        }

        var hillshade = new RasterHillshadeRequestDto
        {
            Width = dim,
            Height = dim,
            CellSize = 30,
            AzimuthDegrees = 315,
            AltitudeDegrees = 45,
            Elevation = elevation
        };

        // The heatmap kernel sweeps the whole grid per point, so cost is points x cells. 200
        // points over 250,000 cells lands in the target range; 10,000 (the service ceiling)
        // would take ten seconds an iteration.
        const int heatmapPoints = 200;
        var rng = new Lcg(20_260_303u);
        var points = new List<WeightedPointDto>(heatmapPoints);
        for (var i = 0; i < heatmapPoints; i++)
        {
            points.Add(new WeightedPointDto
            {
                X = rng.Range(-117.40, -117.05),
                Y = rng.Range(33.90, 34.20),
                Weight = rng.Range(0.25, 4.0)
            });
        }

        var heatmap = new HeatmapRequestDto
        {
            Width = 500,
            Height = 500,
            MinX = -117.40,
            MinY = 33.90,
            MaxX = -117.05,
            MaxY = 34.20,
            Radius = 0.02,
            Points = points
        };

        return
        [
            new Workload
            {
                Kernel = "raster_terrain_kernel",
                Name = $"hillshade, {hillshadeCalls} x {dim}x{dim} elevation grid",
                Run = async token =>
                {
                    var checksum = 0.0;
                    var native = true;
                    for (var c = 0; c < hillshadeCalls; c++)
                    {
                        var r = await service.GenerateHillshadeAsync(hillshade, token);
                        native &= r.NativeAccelerated;
                        checksum = FoldIds(r.Intensities.Select(b => (int)b));
                    }
                    return new RunOutcome(checksum, NativeAccelerated: native);
                }
            },
            new Workload
            {
                Kernel = "raster_terrain_kernel",
                Name = $"Gaussian heatmap, 500x500 over {heatmapPoints} points",
                Run = async token =>
                {
                    var r = await service.GenerateHeatmapAsync(heatmap, token);
                    return new RunOutcome(Fold(r.Values), NativeAccelerated: r.NativeAccelerated);
                }
            }
        ];
    }

    // ---------------------------------------------------------------- spatial_graph_engine

    private static async Task<Workload[]> GraphAsync(CancellationToken ct)
    {
        var service = new SpatialGraphService(NullLogger<SpatialGraphService>.Instance);
        var graph = await service.GetRedlandsGraphAsync(ct);

        var origin = graph.DestinationNodeId;

        // 40 origins and 40 destinations spread evenly across the node list, so the matrix
        // covers the whole network rather than one dense corner of it.
        var stride = Math.Max(1, graph.Nodes.Count / 40);
        var sources = new List<int>();
        for (var i = 0; i < graph.Nodes.Count && sources.Count < 40; i += stride)
            sources.Add(graph.Nodes[i].Id);
        var targets = new List<int>(sources);

        return
        [
            new Workload
            {
                Kernel = "spatial_graph_engine",
                Name = $"one-to-all Dijkstra, 40 x ({graph.Nodes.Count:N0} nodes / {graph.Edges.Count:N0} edges)",
                Run = async token =>
                {
                    // A single search on this network is under a millisecond after the CSR
                    // rewrite, which is timer noise; 40 searches is a measurable unit.
                    var checksum = 0.0;
                    for (var c = 0; c < 40; c++)
                    {
                        var d = await service.ComputeDistancesAsync(graph, origin, token);
                        checksum = Fold(d.Where(double.IsFinite));
                    }
                    return new RunOutcome(checksum);
                }
            },
            new Workload
            {
                Kernel = "spatial_graph_engine",
                Name = "40x40 cost matrix over the same network, 10 builds",
                Run = async token =>
                {
                    var checksum = 0.0;
                    for (var c = 0; c < 10; c++)
                    {
                        var m = await service.ComputeDistanceMatrixAsync(graph, sources, targets, token);
                        checksum = Fold(m.Where(double.IsFinite));
                    }
                    return new RunOutcome(checksum);
                }
            }
        ];
    }

    // ---------------------------------------------------------------- spatial_cluster_kernel

    private static Workload[] Cluster()
    {
        var service = new SpatialClusterService(NullLogger<SpatialClusterService>.Instance);

        // 5,000 points is the service ceiling.
        const int pointCount = 5_000;
        const int calls = 8;

        var rng = new Lcg(20_260_404u);
        var points = new List<ClusterPointDto>(pointCount);

        // Twelve Gaussian-ish blobs plus uniform noise, so DBSCAN actually has structure to
        // find and the core/border/noise branches are all exercised.
        for (var b = 0; b < 12; b++)
        {
            var cx = rng.Range(-4.0, 4.0);
            var cy = rng.Range(-4.0, 4.0);
            for (var i = 0; i < 360; i++)
            {
                points.Add(new ClusterPointDto
                {
                    X = cx + (rng.NextDouble() + rng.NextDouble() + rng.NextDouble() - 1.5) * 0.30,
                    Y = cy + (rng.NextDouble() + rng.NextDouble() + rng.NextDouble() - 1.5) * 0.30
                });
            }
        }
        while (points.Count < pointCount)
            points.Add(new ClusterPointDto { X = rng.Range(-5.0, 5.0), Y = rng.Range(-5.0, 5.0) });

        var request = new DbscanRequestDto { Epsilon = 0.16, MinPoints = 6, Points = points };

        return
        [
            new Workload
            {
                Kernel = "spatial_cluster_kernel",
                Name = $"DBSCAN, {calls} x {pointCount:N0} points, eps 0.16 / minPts 6",
                Run = async token =>
                {
                    var checksum = 0.0;
                    var native = true;
                    for (var c = 0; c < calls; c++)
                    {
                        var r = await service.RunDbscanAsync(request, token);
                        native &= r.NativeAccelerated;
                        checksum = r.ClusterCount * 1_000_003.0 + r.NoiseCount
                            + FoldIds(r.Points.Select(p => p.ClusterId));
                    }
                    return new RunOutcome(checksum, NativeAccelerated: native);
                }
            }
        ];
    }

