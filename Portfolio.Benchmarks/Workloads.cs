    private static async Task<Workload[]> FacilityLocationAsync(CancellationToken ct)
    {
        var graphService = new SpatialGraphService(NullLogger<SpatialGraphService>.Instance);
        var service = new ResponseCoverageService(graphService, NullLogger<ResponseCoverageService>.Instance);

        var shipped = await service.GetScenarioAsync(ct);
        var graph = await graphService.GetRedlandsGraphAsync(ct);

        // The shipped scenario solves in well under a millisecond, which is timer noise. A
        // larger instance is built from real network nodes so the distance matrix is still a
        // genuine road-network matrix rather than Euclidean filler.
        var rng = new Lcg(20_260_606u);
        var demand = new List<DemandPointDto>(1_400);
        for (var i = 0; i < 1_400; i++)
        {
            var node = graph.Nodes[rng.Range(0, graph.Nodes.Count)];
            demand.Add(new DemandPointDto
            {
                Id = i + 1,
                NodeId = node.Id,
                Latitude = node.Latitude,
                Longitude = node.Longitude,
                CallVolume = rng.Range(1, 40)
            });
        }

        var candidates = new List<CandidateSiteDto>(64);
        for (var i = 0; i < 64; i++)
        {
            var node = graph.Nodes[rng.Range(0, graph.Nodes.Count)];
            candidates.Add(new CandidateSiteDto
            {
                Id = i + 1,
                NodeId = node.Id,
                Label = $"Site {i + 1:D2}",
                Latitude = node.Latitude,
                Longitude = node.Longitude,
                IsExisting = i < 3
            });
        }

        OptimizeCoverageRequestDto Request(int objectiveMode) => new()
        {
            DemandPoints = demand,
            Candidates = candidates,
            StationCount = 8,
            ObjectiveMode = objectiveMode,
            AvgSpeedKmh = 40,
            FirstThresholdMinutes = 4,
            SecondThresholdMinutes = 8,
            MaxIterations = 500,
            ExistingStationIds = [1, 2, 3]
        };

        var weightedMean = Request(0);
        var p90 = Request(1);

        // The p-median search converges in a handful of passes, so a single solve is well
        // under the timer's useful range. The workload is `repeats` solves and the reported
        // time is the summed solver stage across them — the matrix build in between is
        // excluded from the total either way.
        async Task<RunOutcome> ScoreAsync(OptimizeCoverageRequestDto request, int repeats, CancellationToken token)
        {
            var stageMs = 0.0;
            var checksum = 0.0;
            var native = true;
            for (var i = 0; i < repeats; i++)
            {
                var r = await service.OptimizeAsync(request, token);
                stageMs += r.SolveMs;
                native &= r.NativeAccelerated;
                checksum = FoldIds(r.ChosenCandidateIds)
                    + r.Optimized.MeanMinutes + r.Optimized.P50Minutes + r.Optimized.P90Minutes
                    + Fold(r.IterationObjectives)
                    + FoldIds(r.Assignments.Select(a => a.AssignedCandidateId)) * 1e-9;
            }
            return new RunOutcome(checksum, stageMs, native);
        }

        var shippedRequest = new OptimizeCoverageRequestDto
        {
            DemandPoints = shipped.DemandPoints,
            Candidates = shipped.Candidates,
            StationCount = 4,
            ObjectiveMode = 1,
            AvgSpeedKmh = 40,
            FirstThresholdMinutes = 4,
            SecondThresholdMinutes = 8,
            MaxIterations = 50,
            ExistingStationIds = shipped.ExistingStationIds
        };

        const string note = "solver stage only (service-reported SolveMs); the shared road-network distance-matrix build is excluded";

        return
        [
            new Workload
            {
                Kernel = "facility_location_kernel",
                Name = "p-median 1,400x64, p=8, weighted mean, 10 solves",
                Note = note,
                Run = token => ScoreAsync(weightedMean, 10, token)
            },
            new Workload
            {
                Kernel = "facility_location_kernel",
                Name = "p-median 1,400x64, p=8, weighted p90",
                Note = note,
                Run = token => ScoreAsync(p90, 1, token)
            },
            new Workload
            {
                Kernel = "facility_location_kernel",
                Name = $"shipped scenario {shipped.DemandPoints.Count}x{shipped.Candidates.Count}, p=4, p90, 4 solves",
                Note = note,
                Run = token => ScoreAsync(shippedRequest, 4, token)
            }
        ];
    }

